package projetoclasses;

public class ClasseIPTU {

	//atributos
	private String codigoImovel;
	private String nomeProprietario;
	private String setor;
	private String endereco;
	private int numeroImovel;
	private double areaImovel;
	private double areaContruida;
	private String getCodigoImovel() {
		return codigoImovel;
	}
	public void setCodigoImovel(String codigoImovel) {
		this.codigoImovel = codigoImovel;
	}
	public String getNomeProprietario() {
		return nomeProprietario;
	}
	public void setNomeProprietario(String nomeProprietario) {
		this.nomeProprietario = nomeProprietario;
	}
	public String getSetor() {
		return setor;
	}
	public void setSetor(String setor) {
		this.setor = setor;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public int getNumeroImovel() {
		return numeroImovel;
	}
	public void setNumeroImovel(int numeroImovel) {
		this.numeroImovel = numeroImovel;
	}
	public double getAreaImovel() {
		return areaImovel;
	}
	public void setAreaImovel(double areaImovel) {
		this.areaImovel = areaImovel;
	}
	public double getAreaContruida() {
		return areaContruida;
	}
	public void setAreaContruida(double areaContruida) {
		this.areaContruida = areaContruida;
	}
	
	

	
}
